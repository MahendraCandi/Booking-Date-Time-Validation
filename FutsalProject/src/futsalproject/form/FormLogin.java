    private void login(){
        EntityManager em=null;
        try {
            em=FutsalProject.emf.createEntityManager();
            Query q=em.createQuery("SELECT u FROM DataUser u WHERE u.kdUser = :kodeUser");
            q.setParameter("kodeUser", txtLogin.getText());
            List<DataUser> list = q.getResultList();
            if(!list.isEmpty()){
                for(DataUser us : list){
                    nama = us.getNmUser();
                    hakAkses = us.getHakAkses();
                    kodeUser = us.getKdUser();
                    password = us.getPassword();
                }
                if(txtPassword.getText().equals(password)){
                    DataUser userLogin = new DataUser();
                    userLogin.setKdUser(kodeUser);
                    userLogin.setHakAkses(hakAkses);
                    userLogin.setNmUser(nama);
                    
                    FormUtama formUtama = new FormUtama(userLogin);
                    
                    JOptionPane.showMessageDialog(this, "Selamat datang, "+nama+"","Welcome", JOptionPane.INFORMATION_MESSAGE);
                    if(hakAkses.equalsIgnoreCase("Pemilik")){
                        formUtama.getMenuTransaksi().setVisible(false);
                        formUtama.getMenuAkun().setVisible(false);
                        formUtama.getMenuLapangan().setVisible(false);
                        formUtama.getMenuPelanggan().setVisible(false);
                    }else if(hakAkses.equalsIgnoreCase("Kasir")){
                        formUtama.getMenuUser().setVisible(false);
                        formUtama.getMenuUtility().setVisible(false);
                    }
                    this.dispose();
                    formUtama.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "Password salah!");
                    txtPassword.setText("");
                    txtPassword.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Kode User tidak ditemukan!");
                txtLogin.setText("");
                txtPassword.setText("");
                txtLogin.requestFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

